# 7.tresintervalos - Verifica si cinco números son iguales

print("Ingrese cinco números:")
n1 = int(input())
n2 = int(input())
n3 = int(input())
n4 = int(input())
n5 = int(input())

if n1 == n2 == n3 == n4 == n5:
    print("Los cinco números son iguales")
else:
    print("Los números son diferentes")
